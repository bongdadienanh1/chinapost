public class AuthorityPageKey {

    private Long authorityId;

    private Long pageId;

    public AuthorityPageKey() {
    }

    public AuthorityPageKey(Long authorityId, Long pageId) {
        this.authorityId = authorityId;
        this.pageId = pageId;
    }

    public Long getAuthorityId() {
        return authorityId;
    }

    public void setAuthorityId(Long authorityId) {
        this.authorityId = authorityId;
    }

    public Long getPageId() {
        return pageId;
    }

    public void setPageId(Long pageId) {
        this.pageId = pageId;
    }
}
