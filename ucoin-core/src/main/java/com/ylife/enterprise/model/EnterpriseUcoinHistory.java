package com.ylife.enterprise.model;

import java.math.BigDecimal;
import java.util.Date;

public class EnterpriseUcoinHistory {

    public EnterpriseUcoinHistory() {
    }

    public EnterpriseUcoinHistory(Long id, Long enterpriseId, BigDecimal balancePrice, Date createTime) {
        this.id = id;
        this.enterpriseId = enterpriseId;
        this.balancePrice = balancePrice;
        this.createTime = createTime;
    }

    private Long id;

    private Long enterpriseId;

    private BigDecimal balancePrice;

    private Date createTime;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getEnterpriseId() {
        return enterpriseId;
    }

    public void setEnterpriseId(Long enterpriseId) {
        this.enterpriseId = enterpriseId;
    }

    public BigDecimal getBalancePrice() {
        return balancePrice;
    }

    public void setBalancePrice(BigDecimal balancePrice) {
        this.balancePrice = balancePrice;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }
}